      private void loadData() {
       try{
           ProductDAO userDAO = new ProductDAO();
           table2.setModel(userDAO.buildTableModel(userDAO.getQueryResult()));
           table2.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                public void valueChanged(ListSelectionEvent event) {
                    if (!event.getValueIsAdjusting()) {
                        int selectedRow = table2.getSelectedRow();

                        // Check if a row is selected
                        if (selectedRow != -1 && selectedRow < table2.getRowCount()) {
                            Object value = table2.getValueAt(selectedRow, 3);
                            try {
                               txtSubCat.setText(value.toString());
                            } catch (Exception e) {
                                e.printStackTrace();
                            }
                        }
                    }
                }
            });
       }catch(Exception ex){
           ex.printStackTrace();
       }
//        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
    }
