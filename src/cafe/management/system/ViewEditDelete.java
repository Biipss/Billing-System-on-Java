    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(btnUpdate.getText().equalsIgnoreCase("Edit")){
            btnUpdate.setText("Update");
            Product userEdit = new ProductDAO().editUser(table2);
            txtId.setText(String.valueOf(userEdit.getId()));
            txtId.setEditable(false);
            txtName.setText(userEdit.getName());
//            cbCategory.addItem(userEdit.getCategory());

            txtPrice.setText(String.valueOf(userEdit.getPrice()));
        }else{
            btnUpdate.setText("Edit");
            Product userUpdate = new Product();
            userUpdate.setId(Integer.parseInt(txtId.getText()));
            userUpdate.setName(txtName.getText());
            userUpdate.setSub_category_id(Integer.parseInt(txtSubCat.getText()));
            userUpdate.setPrice(Integer.parseInt(txtPrice.getText()));
            new ProductDAO().updateUser(userUpdate);
            loadData();
           txtId.setEditable(true);
            clearFields();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

